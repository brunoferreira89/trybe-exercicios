// Lanchonete da Trybe

// 1 - Atribuir variável para o pedido:

let pedido = 6;

// 2 - Utilizando o switch/case para dar as alternativas no código:

switch (pedido) {
    case 1:
        console.log('1 - Trybe Lanche Feliz');
        break;
    case 2:
        console.log('2 - McTrybe');
        break;
    case 3:
        console.log('3 - Trybe Whopper');
        break;
    case 4:
        console.log('4 - X-Trybe');
        break;
    case 5:
        console.log('5 - Triplo Trybe com JS');
        break;
    default:
        console.log('Ainda não possuímos esta opção :(');
}